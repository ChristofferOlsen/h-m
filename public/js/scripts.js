$(document).ready(function(){       
  var scroll_start = 0;
  var startchange = $('.start');
  if (startchange.length){
    $(document).scroll(function() { 
      scroll_start = $(this).scrollTop();
      if(scroll_start > 100) {
        $(".head-wrapper").addClass('is-active');
      } else {
        $(".head-wrapper").removeClass('is-active');
      }
    });
  }
});

